/**
 * Дозагрузка скриптов для части страницы которая не попала в критическую зону
 */
(function (){
    var $ = jQuery ;
    var $body = $('body')
    var $document = $('document')
    var $window = $('window')

    var loadLater = function () {
        $body.trigger('__loadLaterCss');

        window.removeEventListener("scroll", loadLater,true);
        window.removeEventListener("mousemove", loadLater,true);

    }
    // setTimeout(loadLater , 500 )
    window.addEventListener("scroll", loadLater,true );
    window.addEventListener("mousemove", loadLater,true );




})()



















